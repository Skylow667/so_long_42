int	ft_strcmp(char *s1, char *s2)
{
	int	cnt_1;

	cnt_1 = 0;
	while (s1[cnt_1] != '\0' && s2[cnt_1] != '\0' && s1[cnt_1] == s2[cnt_1])
		cnt_1++;
	return (s1[cnt_1] - s2[cnt_1]);
}
