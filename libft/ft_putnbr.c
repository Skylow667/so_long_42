#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	nb_temp;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = nb *(-1);
	}
	nb_temp = nb + '0';
	if (nb >= 0 && nb <= 9)
		write(1, &nb_temp, 1);
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
}
